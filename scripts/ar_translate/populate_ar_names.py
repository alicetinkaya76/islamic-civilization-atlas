def main():
    dry_run = "--dry-run" in sys.argv
    
    with open(DB_PATH, "r", encoding="utf-8") as f:
        db = json.load(f)
    
    total = 0
    
    # Apply dynasty names
    name_maps = [
        ("dynasties", DYNASTY_AR),
        ("battles", BATTLE_AR),
        ("events", EVENT_AR),
        ("cities", CITY_AR),
        ("monuments", MONUMENT_AR),
        ("scholars", SCHOLAR_AR),
        ("routes", ROUTE_AR),
        ("madrasas", MADRASA_AR),
    ]
    
    for coll, ar_map in name_maps:
        if not ar_map:
            print(f"  ⏭ {coll}: no data (will be filled by API or ext script)")
            continue
        count = 0
        for item in db[coll]:
            eid = item.get("id")
            if eid in ar_map and ar_map[eid]:
                if dry_run:
                    if count < 3:
                        print(f"    {coll}#{eid}: {item.get('tr','')} → {ar_map[eid]}")
                else:
                    item["ar"] = ar_map[eid]
                count += 1
        total += count
        print(f"  ✓ {coll}: {count}/{len(db[coll])} names {'(dry)' if dry_run else 'written'}")
    
    if not dry_run:
        with open(DB_PATH, "w", encoding="utf-8") as f:
            json.dump(db, f, ensure_ascii=False, indent=2)
        print(f"\n✓ Total: {total} AR names written to db.json")
    else:
        print(f"\n[DRY RUN] Would write {total} AR names")


if __name__ == "__main__":
    main()
